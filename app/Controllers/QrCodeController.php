    public function barcode()
    {
        // create new PDF document
        $pdf = new TCPDF('P', PDF_UNIT, PDF_PAGE_FORMAT, true, 'UTF-8', false);

        $pdf->SetHeaderMargin(0);
        $pdf->SetFooterMargin(0);
        $pdf->SetPrintHeader(false);
        $pdf->setPrintFooter(false);


        // add a page
        $pdf->setTopMargin(20);
        $pdf->AddPage();
        // define barcode style
        $style = array(
            'position' => '',
            'align' => 'C',
            'stretch' => false,
            'fitwidth' => true,
            'cellfitalign' => '',
            'border' => true,
            'hpadding' => 'auto',
            'vpadding' => 'auto',
            'fgcolor' => array(0, 0, 0),
            'bgcolor' => false, //array(255,255,255),
            'text' => true,
            'font' => 'helvetica',
            'fontsize' => 8,
            'stretchtext' => 0
        );

        $dataFromDatabase = $this->getDataFromDatabase();

        // Set posisi awal
        $x = 20;
        $y = 3;

        // Loop melalui data dan menambahkan barcode ke dokumen PDF
        foreach ($dataFromDatabase as $key => $row) {
            if (($key + 1) % 2 == 0) {
                $pdf->writeHTMLCell(0, 0, $x + 16, $y + 16, '<p>' . $row['val1'] . '</p>');
                $pdf->write1DBarcode($row['val0'], 'C128', $x, $y, 60, 16, "", $style, 'T');
                $y += 25.5;
                $x = 20;
            } else {

                $pdf->writeHTMLCell(0, 0, $x + 16, $y + 16, '<p>' . $row['val1'] . '</p>');
                $pdf->write1DBarcode($row['val0'], 'C128', $x, $y, 60, 16, "", $style, 'T');
                $x += 85;
            }
            if (($key + 1) % 20 == 0) {
                $pdf->AddPage();
                $y = 3;
            }
        }
        //Close and output PDF document
        $pdf->Output('example_027.pdf', 'D');
    }


